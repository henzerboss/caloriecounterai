function articleFor(name) {
  const lower = name.trim().toLowerCase();
  const uncountableHints = ['rice', 'pasta', 'oatmeal', 'yogurt', 'milk', 'cheese', 'butter', 'oil', 'hummus', 'ketchup', 'honey', 'jam', 'coffee', 'tea', 'lemonade', 'water', 'juice', 'salmon', 'bread', 'cod', 'tilapia', 'crab', 'candy', 'couscous', 'barley', 'bulgur', 'buckwheat', 'guacamole', 'salsa'];
  if (lower.endsWith('s') || uncountableHints.some((hint) => lower.includes(hint))) return lower;
  const first = lower[0];
  if (['a', 'e', 'i', 'o', 'u'].includes(first)) return `an ${lower}`;
  return `a ${lower}`;
}

